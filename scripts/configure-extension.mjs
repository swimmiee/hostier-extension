import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const TARGETS = {
  dev: {
    defaultHostierUrl: "http://localhost:5173",
  },
  prod: {
    defaultHostierUrl: "https://hostier.ai",
  },
};

const PLATFORM_HOST_PERMISSIONS = [
  "https://web.33m2.co.kr/*",
  "https://host.enko.kr/*",
  "https://*.liveanywhere.me/*",
];

function normalizeHostierUrl(rawUrl) {
  const trimmed = rawUrl.trim();
  const parsed = new URL(trimmed);

  if (parsed.pathname !== "/" || parsed.search || parsed.hash) {
    throw new Error("HOSTIER_WEB_URL must be an origin, for example https://hostier.ai");
  }

  return parsed.origin;
}

function buildManifest(hostierUrl) {
  const hostierMatch = `${hostierUrl}/*`;
  const localhostMatch = "http://localhost:5173/*";
  const installDetectionMatches = [hostierMatch, localhostMatch];

  return {
    manifest_version: 3,
    name: "Hostier",
    version: "2.0.0",
    default_locale: "ko",
    description: "__MSG_extDescription__",
    permissions: ["cookies", "storage", "scripting", "tabs"],
    host_permissions: [hostierMatch, localhostMatch],
    optional_host_permissions: [...PLATFORM_HOST_PERMISSIONS],
    icons: {
      "16": "icon16.png",
      "48": "icon48.png",
      "128": "icon128.png",
    },
    action: {
      default_popup: "popup.html",
      default_icon: {
        "16": "icon16.png",
        "48": "icon48.png",
        "128": "icon128.png",
      },
    },
    background: {
      service_worker: "background.js",
    },
    content_scripts: [
      {
        matches: installDetectionMatches,
        js: ["install-detector.js"],
        run_at: "document_idle",
      },
    ],
  };
}

function buildConfig(hostierUrl, target) {
  return `// Generated by scripts/configure-extension.mjs\nself.HOSTIER_EXTENSION_CONFIG = Object.freeze({\n  hostierUrl: ${JSON.stringify(hostierUrl)},\n  target: ${JSON.stringify(target)},\n});\n`;
}

function writeFileIfChanged(path, nextContent) {
  try {
    const currentContent = readFileSync(path, "utf8");
    if (currentContent === nextContent) {
      return false;
    }
  } catch {
    // File may not exist yet. Write it below.
  }

  writeFileSync(path, nextContent);
  return true;
}

const target = process.argv[2];
if (!target || !(target in TARGETS)) {
  throw new Error("Usage: node scripts/configure-extension.mjs <dev|prod>");
}

const hostierUrl = normalizeHostierUrl(
  process.env.HOSTIER_WEB_URL || TARGETS[target].defaultHostierUrl,
);

const manifestPath = resolve("manifest.json");
const configPath = resolve("config.js");

writeFileIfChanged(
  manifestPath,
  `${JSON.stringify(buildManifest(hostierUrl), null, 2)}\n`,
);
writeFileIfChanged(
  configPath,
  buildConfig(hostierUrl, target),
);

console.log(`Configured extension for ${target}: ${hostierUrl}`);
